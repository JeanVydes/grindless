"use client"
import useOAuthStore from "@/stores/oauth"
import DashboardMenu from "./dashboard-menu"
import DashboardTopBar from "./dashboard-top-bar"
import DashboardServices from "./dashboard-services";
import { useState } from "react";
import SummaryService from "@/components/dashboard/sections/services/summary/summary";
import { Sections } from "./sections/sections";
import Settings from "./sections/settings";
import Credits from "./sections/credits";

export function Dashboard() {
    let oauthStore = useOAuthStore();
    let [selectedSection, setSelectedSection] = useState<Sections>(Sections.Main);

    if (oauthStore.checked && !oauthStore.authenticated) {
        window.location.href = "/";
        return (<></>)
    }

    return (
        <>
            <div className="flex min-h-screen w-full bg-background text-foreground">
                <DashboardMenu setSelectedSection={setSelectedSection} />
                <div className="w-full flex flex-col sm:gap-4 sm:py-4 sm:pl-14">
                    <DashboardTopBar setSelectedSection={setSelectedSection} oauthStore={oauthStore} selectedSection={selectedSection} />
                    {selectedSection === Sections.Main && <DashboardServices setSelectedSection={setSelectedSection} />}
                    {selectedSection === Sections.Credits && <Credits />}
                    {selectedSection === Sections.Settings && <Settings />}
                    {selectedSection === Sections.ServiceSummaryText && <SummaryService oauthStore={oauthStore} />}
                </div>
            </div>
        </>
    )
}


